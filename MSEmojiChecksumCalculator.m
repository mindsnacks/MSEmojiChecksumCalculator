@implementation MSEmojiChecksumCalculator

- (NSString *)checksumForFile:(NSString *)path
{
    return [self checksumForInputStream:[NSInputStream inputStreamWithFileAtPath:path]];
}

- (NSString *)checksumForData:(NSData *)data
{
    return [self checksumForInputStream:[NSInputStream inputStreamWithData:data]];
}

- (NSString *)checksumForInputStream:(NSInputStream *)stream
{
    NSData *data = [self MD5FromInputStream:stream];
    NSMutableString *emojiChecksum = [[NSMutableString new] autorelease];

    const char *bytes = (const char *)[data bytes];

    char byte = 0;
    for (int i = 0; i < CC_MD5_DIGEST_LENGTH; i++) {

        byte ^= bytes[i];

        if (((i + 1) % EMOJI_CHECKSUM_LENGTH) == 0)
        {
            // make a char
            NSUInteger idx = byte % [MSAllEmojiCharacterString length];
            NSRange emojiRange = [MSAllEmojiCharacterString rangeOfComposedCharacterSequenceAtIndex:idx];
            [emojiChecksum appendString:[MSAllEmojiCharacterString substringWithRange:emojiRange]];
            byte = 0;
        }
    }

    return emojiChecksum;
}

#define CHUNK_SIZE 1024
- (NSData *)MD5FromFile:(NSString*)path
{
    return [self MD5FromInputStream:[NSInputStream inputStreamWithFileAtPath:path]];
}

- (NSData *)MD5FromInputStream:(NSInputStream *)stream
{
    [stream open];
    CC_MD5_CTX md5;

	CC_MD5_Init(&md5);
    u_int8_t *data = malloc(CHUNK_SIZE);

	while(stream.streamStatus != NSStreamStatusAtEnd && stream.streamStatus != NSStreamStatusError)
	{
        NSUInteger read = [stream read:data maxLength:CHUNK_SIZE];
		CC_MD5_Update(&md5, data, (int)read);
	}

    free(data);
	MD5 digest;
	CC_MD5_Final(digest, &md5);
    [stream close];
    
    return [NSData dataWithBytes:digest length:CC_MD5_DIGEST_LENGTH];
}

@end
